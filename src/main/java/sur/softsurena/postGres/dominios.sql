 AS TIME
 DEFAULT CURRENT_TIME;

CREATE DOMAIN D_ID
 AS INTEGER
 DEFAULT NULL
 CHECK (value >= 0);

CREATE DOMAIN D_IDUSUARIO
 AS VARCHAR(31)
 DEFAULT current_user
 NOT NULL;
 
