 AS CHAR(1)
 DEFAULT 'I'
 NOT NULL
 CHECK (UPPER(value) in('I', 'D'));
 
CREATE DOMAIN D_MEDIDA
 AS NUMERIC(18,2)
 DEFAULT 0.00;
 
CREATE DOMAIN D_NOMBRES
